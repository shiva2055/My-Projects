public enum Direction {
	UP(0, -1), 
	DOWN(0, 1),
	LEFT(-1, 0), 
	RIGHT(1,0);

	private double x, y;
	
	 /***************************************************************************************************************
	 * Constructor:  no argument
	 * 				 calls second constructor 
	 ************************************************************************************************************** */
	Direction()
	{
		this(1,1);
	}
	 /***************************************************************************************************************
	 * Constructor:  two argument
	 * 				 values for the data fields are initilizied to local values 
	 ************************************************************************************************************** */
	Direction(double x, double y)
	{
		this.x = x;
		this.y = y;
	}
	/********************************************************************************
	 * accessor: double
	 *           returns x
	 *********************************************************************************/
	public double getX() {
		return x;
	}
	/********************************************************************************
	 * accessor: double
	 *           returns y
	 *********************************************************************************/
	public double getY() {
		return y;
	}
	/********************************************************************************
	 * mutator: setX
	 *          sets global value of X to local Y
	 *********************************************************************************/
	public void setX(double x) {
		this.x = x;
	}
	/********************************************************************************
	 * mutator: setY
	 *           sets global value of Y to local Y
	 *********************************************************************************/
	public void setY(double y) {
		this.y = y;
	}
	
	
	
}
